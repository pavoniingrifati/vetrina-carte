const fs=require('fs');
const path=require('path');
const root=path.resolve(__dirname,'..');
const readJson=file=>JSON.parse(fs.readFileSync(path.join(root,file),'utf8'));
const clubs=readJson('data/storico/club-storiche.json');
const players=readJson('data/storico/giocatori-storici.json');
const manifest=readJson('data/storico/manifest-squadre-storiche.json');
const validation=readJson('data/storico/VALIDAZIONE-LEGEND-40.json');
const setup=fs.readFileSync(path.join(root,'assets/season/04-setup-and-draft.js'),'utf8');
const opp=fs.readFileSync(path.join(root,'assets/season/05-opponents-and-chaos.js'),'utf8');
const runtime=fs.readFileSync(path.join(root,'assets/season/14-runtime.js'),'utf8');
const tests=[];
const check=(name,condition,details='')=>tests.push({name,ok:Boolean(condition),details});
const historical=clubs.filter(c=>c.historical);
const ids=new Set(historical.map(c=>String(c.id)));
const counts={}; for(const p of players)counts[p.club]=(counts[p.club]||0)+1;
check('40 club storici',historical.length===40,`trovati ${historical.length}`);
check('Club utente più 40 Legend',clubs.length===41,`record club ${clubs.length}`);
check('796 giocatori Legend',players.length===796,`giocatori ${players.length}`);
check('Riferimenti club validi',players.every(p=>ids.has(String(p.club))));
check('Rose utilizzabili',historical.every(c=>(counts[c.id]||0)>=14),Math.min(...historical.map(c=>counts[c.id]||0))+' giocatori nella rosa più corta');
check('OVR estesi validi',players.every(p=>Number.isInteger(p.ovr)&&p.ovr>=1&&p.ovr<=120),`range ${Math.min(...players.map(p=>p.ovr))}-${Math.max(...players.map(p=>p.ovr))}`);
check('Pelé resta 116',players.some(p=>p.name==='Pelé'&&p.ovr===116));
check('Manifest aggiornato',manifest.historicalClubs===40&&manifest.playersTotal===796&&manifest.leagueFormat?.randomHistoricalOpponents===19);
check('Validazione dati superata',validation.valid===true&&validation.errors.length===0);
check('Scelta 19 avversarie casuali',opp.includes('shuffle(CLUBS.filter(club=>club.id!==userClub.id)).slice(0,19)'));
check('Setup comunica 40 squadre',setup.includes('40 squadre storiche')&&setup.includes('19 avversarie casuali'));
check('Validazione runtime a 41 record',runtime.includes('minimumClubCount:41')&&runtime.includes('expectedClubCount:41'));
const failed=tests.filter(t=>!t.ok);
console.log(JSON.stringify({name:'Test Legend 40 squadre',passed:tests.length-failed.length,total:tests.length,failed,tests},null,2));
if(failed.length)process.exit(1);
